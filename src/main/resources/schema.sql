drop table if exists article;
drop table if exists user;

create table article
(
    id        bigint generated by default as identity,
    writer       varchar(50),
    title        varchar(50),
    contents     text,
    datetime     varchar(50),
    primary key (id)
);

create table user
(
    id        bigint generated by default as identity,
    userid      varchar(50),
    name        varchar(50),
    password    varchar(50),
    email       varchar(100),
    primary key (id)
);

